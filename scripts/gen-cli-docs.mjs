#!/usr/bin/env node
import { spawnSync } from 'node:child_process';
import { writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const cli = resolve(root, 'dist', 'cli', 'index.js');
const outFile = resolve(root, 'docs', 'cli.md');

if (!existsSync(cli)) {
  throw new Error(`CLI build output not found at ${cli}. Run npm run build first.`);
}

const sections = [
  { title: 'Top-level help', cmd: ['--help'] },
  { title: 'new', cmd: ['new', '--help'] },
  { title: 'list', cmd: ['list', '--help'] },
  { title: 'get', cmd: ['get', '--help'] },
  { title: 'enable', cmd: ['enable', '--help'] },
  { title: 'disable', cmd: ['disable', '--help'] },
  { title: 'delete', cmd: ['delete', '--help'] },
  { title: 'run-now', cmd: ['run-now', '--help'] },
  { title: 'logs', cmd: ['logs', '--help'] },
  { title: 'export', cmd: ['export', '--help'] },
  { title: 'import', cmd: ['import', '--help'] },
  { title: 'doctor', cmd: ['doctor', '--help'] },
  { title: 'daemon', cmd: ['daemon', '--help'] },
  { title: 'daemon start', cmd: ['daemon', 'start', '--help'] },
  { title: 'daemon stop', cmd: ['daemon', 'stop', '--help'] },
  { title: 'daemon status', cmd: ['daemon', 'status', '--help'] },
  { title: 'daemon reload', cmd: ['daemon', 'reload', '--help'] },
  { title: 'daemon restart', cmd: ['daemon', 'restart', '--help'] },
  { title: 'autostart', cmd: ['autostart', '--help'] },
  { title: 'autostart install', cmd: ['autostart', 'install', '--help'] },
  { title: 'autostart remove', cmd: ['autostart', 'remove', '--help'] },
  { title: 'autostart status', cmd: ['autostart', 'status', '--help'] },
  { title: 'dashboard', cmd: ['dashboard', '--help'] },
  { title: 'uninstall', cmd: ['uninstall', '--help'] },
  { title: 'mcp', cmd: ['mcp', '--help'] },
];

// eslint-disable-next-line no-control-regex
const stripAnsi = (text) => text.replace(/\u001b\[[0-9;]*m/g, '');
const runHelp = (args) => {
  const result = spawnSync(process.execPath, [cli, ...args], {
    encoding: 'utf-8',
    env: { ...process.env, CRONTICK_MCP_NO_AUTOSTART: '1' },
  });
  const output = `${result.stdout ?? ''}${result.stderr ?? ''}`.trim();
  if (result.status !== 0 && !output) {
    throw new Error(`Failed to collect help for: ${args.join(' ')}`);
  }
  return stripAnsi(output);
};

const lines = [
  '# CLI reference',
  '',
  '> Generated by `node scripts/gen-cli-docs.mjs` from the built CLI help output.',
  '',
  'Use `crontick --json` on most read commands for machine-readable output.',
  '',
];

for (const section of sections) {
  lines.push(`## ${section.title}`);
  lines.push('');
  lines.push('```text');
  lines.push(runHelp(section.cmd));
  lines.push('```');
  lines.push('');
}

mkdirSync(resolve(root, 'docs'), { recursive: true });
writeFileSync(outFile, lines.join('\n'), 'utf-8');
console.log(`Wrote ${outFile}`);
